namespace Domain.Models;

/// <summary>
/// Определяет типы продуктов в системе.
/// </summary>
/// <remarks>
/// <para>
/// Перечисление используется для категоризации продуктов в модели <see cref="Product"/>.
/// Каждый элемент соответствует определённой категории продуктов, которая может
/// использоваться для фильтрации или обработки данных.
/// </para>
/// <para>
/// Значения перечисления хранятся в базе данных как целые числа, соответствующие
/// порядковому номеру элемента (начиная с 0).
/// </para>
/// </remarks>
/// <example>
/// <code>
/// var product = new Product
/// {
///     ProductId = 1,
///     Name = "Молоко",
///     ProductType = ProductType.food
/// };
/// Console.WriteLine($"Продукт: {product.Name}, Тип: {product.ProductType}");
/// </code>
/// </example>
public enum ProductType
{
    /// <summary>
    /// Продукты питания.
    /// </summary>
    /// <remarks>
    /// Используется для продуктов, относящихся к категории продовольствия (например, молоко, хлеб).
    /// </remarks>
    food,

    /// <summary>
    /// Электроника.
    /// </summary>
    /// <remarks>
    /// Используется для электронных устройств и гаджетов (например, смартфоны, ноутбуки).
    /// </remarks>
    electronics,

    /// <summary>
    /// Одежда.
    /// </summary>
    /// <remarks>
    /// Используется для товаров, относящихся к категории одежды (например, футболки, джинсы).
    /// </remarks>
    clothing,

    /// <summary>
    /// Прочие товары.
    /// </summary>
    /// <remarks>
    /// Используется для продуктов, не подпадающих под другие категории (например, книги, игрушки).
    /// </remarks>
    other
}